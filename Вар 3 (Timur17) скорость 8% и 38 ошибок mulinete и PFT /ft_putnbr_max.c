#include "printf.h"

void	ft_putnbr_max(intmax_t n, int f)
{
	if (n / 10 == -922337203685477580 && n % 10 == -8)
	{
		if (f == 1)
			ft_putstr("9223372036854775808");
		else
			ft_putstr("-9223372036854775808");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * (-1);
	}
	if (n > 9)
		ft_putnbr_max(n / 10, 0);
	ft_putchar(n % 10 + 48);
}
