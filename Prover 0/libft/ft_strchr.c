#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;
	size_t	i;

	ch = (char)c;
	i = 0;
	while (s[i] != ch)
	{
		if (!s[i++])
			return (0);
	}
	return ((char *)s + i);
}
